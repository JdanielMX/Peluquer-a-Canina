    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed

        String nombre = txtNombre.getText();
        String color = txtColor.getText();
        String nombreDuenio = txtNombreD.getText();
        String observaciones = txtObserbaciones.getText();
        String tel = txtTel.getText();
        String raza = txtRaza.getText();
        String alergico = cbbAlergico.getItemAt(cbbAlergico.getSelectedIndex());
        String atencio = cbbAtencion.getItemAt(cbbAtencion.getSelectedIndex());

        String errores = "";

        if (nombre.isBlank()) {

            errores += "** El nombre de la mascota no puede estar vacia \n";
        }
        if (color.isBlank()) {

            errores += "** El color de la mascota no puede estar vacio \n";
        }

        if (nombreDuenio.isBlank()) {

            errores += "** El nombre del dueño la mascota no puede estar vacio \n";
        }
        if (observaciones.isBlank()) {

            errores += "** Las observaciones de la mascota no puede estar vacia \n";
        }
        if (tel.isBlank()) {

            errores += "** El telefono del dueño de la mascota no puede estar vacio \n";
        }
        if (nombre.isBlank()) {

            errores += "** La raza de la mascota no puede estar vacia \n";
        }

        if (errores.isBlank()) {

            control.actualizarDatos(mascota,nombre, color, nombreDuenio, observaciones, raza, tel, alergico, atencio);
            JOptionPane.showMessageDialog(null, "Registro actualizado");
            limpiar();
            MostrarDatos ventana = new MostrarDatos();
            ventana.setVisible(true);
            ventana.setLocationRelativeTo(null);
            this.dispose();
        } else {

            JOptionPane.showMessageDialog(null, errores);
        }


    }//GEN-LAST:event_btnGuardarActionPerformed


