    private void cargarDatos(int numeroCliente) {

      mascota = control.obtenerMascota(numeroCliente);

        txtNombre.setText(mascota.getNombre());
        txtColor.setText(mascota.getColor());
        txtNombreD.setText(mascota.getDuenio().getNombre());
        txtObserbaciones.setText(mascota.getObservacion());
        txtTel.setText(mascota.getDuenio().getTelefono());
        txtRaza.setText(mascota.getRaza());
        if (mascota.getAlergico().equals("-")) {

            cbbAlergico.setSelectedIndex(2);

        } else if (mascota.getAlergico().equals("NO")) {

            cbbAlergico.setSelectedIndex(1);

        }
        
           if (mascota.getAtencion().equals("-")) {

            cbbAtencion.setSelectedIndex(2);

        } else if (mascota.getAtencion().equals("NO")) {

            cbbAtencion.setSelectedIndex(1);

        }

    }
