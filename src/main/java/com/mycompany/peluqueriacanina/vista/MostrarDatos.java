    private void cargarTabla() {

        DefaultTableModel tabla = new DefaultTableModel() {

            @Override
            public boolean isCellEditable(int r, int c) {
                return false;
            }
        };

        String[] titulos = {"IDMASCOTA", "NOMBRE", "RAZA", "COLOR", "ALERGICO", "ATENCION", "OBSERVACION", "NOMBRE DUEÑO", "TELEFONO"};

        tabla.setColumnIdentifiers(titulos);

        List<Mascota> lista = new ArrayList();

        lista = control.mostrarMascotas();

        if (!lista.isEmpty()) {

            for (Mascota mascota : lista) {

                Object[] datos = {mascota.getIdMascota(), mascota.getNombre(), mascota.getRaza(), mascota.getColor(),
                    mascota.getAlergico(), mascota.getAtencion(), mascota.getObservacion(),
                    mascota.getDuenio().getNombre(), mascota.getDuenio().getTelefono()};

                tabla.addRow(datos);

            }

        }

        tblMascotas.setModel(tabla);

    }
